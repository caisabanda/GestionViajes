using Microsoft.EntityFrameworkCore;
using GestionViajesCaisabanda.Models;

namespace GestionViajesCaisabanda
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            // CONEXIÓN A MYSQL PARA GEISON
            builder.Services.AddDbContext<ViajesContext>(options =>
                options.UseMySQL(builder.Configuration.GetConnectionString("ConexionViajes")));

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}